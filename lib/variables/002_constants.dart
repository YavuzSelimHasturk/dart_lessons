void main(){

  // Sabitler
  // final, const
  // Java da final - Swift de let - Kotlin de val olarak mevcuttur

  int sayi = 30;
  print(sayi);
  sayi=100;
  print(sayi);


  // kodlamayi calistirdigimizda hafizada olusur
  final int numara = 100;
  print(numara);
  // numara=200; final ile atama yaptigimiz icin degistirmeye izin vermiyor

  // degiskeni tanimladigimizda hafizada olusur
  // genellikle gorsel nesnelerde kullaniriz. button,text vb.
  const int sonuc = 600;
  print(sonuc);
  // sonuc=100;   const ile atama yaptigimiz icin degistirmeye izin vermiyor


}